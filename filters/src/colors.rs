use syntect::highlighting::{Color, FontStyle, Style};

const fn hex(value: u32) -> Color {
    // NOTE: In the current implementation of
    // `syntect::utils::as_latex_escaped`, the `g` and `b` fields are flipped.
    #[allow(clippy::identity_op)]
    Color {
        r: ((value >> 16) & 0xFF) as _,
        g: ((value >> 0) & 0xFF) as _,
        b: ((value >> 8) & 0xFF) as _,
        a: 0xFF,
    }
}
const fn color(value: u32) -> Style {
    Style {
        foreground: hex(value),
        background: Color::WHITE,
        font_style: FontStyle::empty(),
    }
}

